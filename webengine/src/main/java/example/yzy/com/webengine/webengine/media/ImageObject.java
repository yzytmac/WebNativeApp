package example.yzy.com.webengine.webengine.media;

public class ImageObject {
        private int count = 9;
        private String success;
        private String fail;
        private String complete;

        public int getCount() {
            return count;
        }

        public void setCount(int pCount) {
            count = pCount;
        }

        public String getSuccess() {
            return success;
        }

        public void setSuccess(String pSuccess) {
            success = pSuccess;
        }

        public String getFail() {
            return fail;
        }

        public void setFail(String pFail) {
            fail = pFail;
        }

        public String getComplete() {
            return complete;
        }

        public void setComplete(String pComplete) {
            complete = pComplete;
        }
    }